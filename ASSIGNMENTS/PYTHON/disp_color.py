color=input("Enter the color code to display COLOR[R/G/B/Y/W]: ").upper()
if color=='Y':
    print("YELLOW")
elif color=='R':
    print("RED")
elif color=='G':
    print("GREEN")
elif color=='B':
    print("BLUE")
elif color=='W':
    print("WHITE")      